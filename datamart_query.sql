INSERT INTO ANALYSIS.DM_RFM_SEGMENTS 
SELECT
    R.USER_ID
    ,R.RECENCY
    ,F.FREQUENCY
    ,M.MONETARY_VALUE
FROM ANALYSIS.TMP_RFM_RECENCY R
JOIN ANALYSIS.TMP_RFM_FREQUENCY F
    ON R.USER_ID = F.USER_ID
JOIN ANALYSIS.TMP_RFM_MONETARY_VALUE M 
    ON R.USER_ID = M.USER_ID
;

